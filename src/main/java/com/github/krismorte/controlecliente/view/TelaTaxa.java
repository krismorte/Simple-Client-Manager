    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtLocal.getText().equals("") | txtValor.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Todos os campos são obrigatórios");
        } else {
            if (taxaEdicao == null) {
                Taxa taxa = new Taxa();
                taxa.setNome(txtLocal.getText());
                taxa.setValor(new BigDecimal(txtValor.getText()));
                dao.add(taxa);
                JOptionPane.showMessageDialog(rootPane, "Taxa adicionada com sucesso!");
            } else {
                taxaEdicao.setNome(txtLocal.getText());
                taxaEdicao.setValor(new BigDecimal(txtValor.getText()));
                dao.add(taxaEdicao);
                JOptionPane.showMessageDialog(rootPane, "Taxa atualizada com sucesso!");
            }
            resetTela();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        taxas = dao.listAll();
        exibeTabela(taxas);
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        resetTela();
    }//GEN-LAST:event_btnCancelActionPerformed

    public void resetTela() {
        txtLocal.setText("");
        txtValor.setText("");
        taxas = dao.listAll();
        taxaEdicao = null;
        exibeTabela(taxas);        
    }

    private void addEventoBusca(){
        txtBusca.addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent e) {
                if (txtBusca.getText().equals("")) {
                    exibeTabela(taxas);
                } else {
                    List<Taxa> taxasBusca = taxaTable.search(txtBusca.getText());
                    exibeTabela(taxasBusca);                    
                }
            }
        });
    }
    
