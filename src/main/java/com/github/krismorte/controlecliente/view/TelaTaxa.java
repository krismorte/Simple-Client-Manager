    private void exibeTabela(List taxas) {
        taxaTable.setList(taxas);
        taxaTable.setJPanel(panelResultado);
        tabelaTaxa = taxaTable.show();
        tabelaTaxa.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent e) {
                if (e.getClickCount() >= 2) {
                    taxaEdicao = taxaTable.getSelected();
                    txtLocal.setText(taxaEdicao.getNome());
                    txtValor.setText(taxaEdicao.getValor().toPlainString());
                }
            }
        });
    }

